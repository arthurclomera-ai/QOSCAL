def _inventory_item(
    host: HostRecord,
    ports_by_ip: dict[str, list[PortEntry]],
    software_by_ip: dict[str, list[SoftwareEntry]],
    config: SystemConfig,
) -> dict:
    """One OSCAL inventory-item per host IP."""
    comp_uuid = det_uuid(f"component:host:{host.ip_address}")
    system_uuid = config.system_id or det_uuid(f"system:{config.system_name}")

    props: list[dict] = [
        {"name": "ipv4-address",   "value": host.ip_address,       "ns": _NS_OSCAL},
        {"name": "asset-id",       "value": host.ip_address,       "ns": _NS_OSCAL},
        {"name": "asset-type",     "value": "computing-device",    "ns": _NS_OSCAL},
        {"name": "scan-type",      "value": "Nessus",              "ns": _NS_OSCAL},
        {"name": "credentialed-scan", "value": host.credentialed_scan, "ns": _NS_OSCAL},
    ]
    if host.host_key_id:
        props.append({"name": "mac-address", "value": host.host_key_id, "ns": _NS_OSCAL})
    if host.os_name:
        props.append({"name": "operating-system", "value": host.os_name, "ns": _NS_OSCAL})
    if host.netbios_name:
        props.append({"name": "netbios-name", "value": host.netbios_name, "ns": _NS_OSCAL})
    if host.scan_date:
        props.append({"name": "scan-date", "value": host.scan_date, "ns": _NS_OSCAL})

    # Port/Protocol/Service props from PPSM
    for pe in ports_by_ip.get(host.ip_address, []):
        props.append({
            "name": "port-protocol-service",
            "value": f"{pe.port}/{pe.protocol}/{pe.svc_name}",
            "ns": _NS_OSCAL,
        })

    # Implemented-components: link to host component and each software component
    impl_comps = [
        {
            "component-uuid": comp_uuid,
            "remarks": f"Host component for {host.ip_address}",
        }
    ]
    for sw in software_by_ip.get(host.ip_address, []):
        sw_uuid = det_uuid(f"component:software:{sw.software_key or sw.software}")
        impl_comps.append(
            {
                "component-uuid": sw_uuid,
                "remarks": f"Software: {sw.software_key or sw.software}",
            }
        )

    return {
        "uuid": det_uuid(f"inventory:{host.ip_address}"),
        "description": (
            f"Host {host.ip_address} identified in Nessus scan. "
            f"Scan date: {host.scan_date or 'unknown'}."
        ),
        "props": props,
        "implemented-components": impl_comps,
    }


