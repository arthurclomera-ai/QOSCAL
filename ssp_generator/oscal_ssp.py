def _metadata(config: SystemConfig) -> dict:
    party_uuid = det_uuid(f"party:{config.org_name}")
    roles = [{"id": r["id"], "title": r["title"]} for r in config.roles]
    return {
        "title": f"System Security Plan — {config.system_name}",
        "last-modified": _now(),
        "version": config.version,
        "oscal-version": OSCAL_VERSION,
        "roles": roles,
        "parties": [
            {
                "uuid": party_uuid,
                "type": "organization",
                "name": config.org_name,
            }
        ],
        "responsible-parties": [
            {
                "role-id": "prepared-by",
                "party-uuids": [party_uuid],
            }
        ],
        "remarks": (
            "This SSP was generated by the CLaaS OSCAL SSP Generator from "
            "Nessus QVD scan data.  Sections marked 'REQUIRED' must be "
            "completed by the system owner before ATO submission."
        ),
    }


