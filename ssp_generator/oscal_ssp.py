_SEVERITY_ORDER = {"very high": 3, "high": 2, "medium": 1, "low": 0, "info": -1}


def _implementation_status(control_id: str, hosts: list[HostRecord]) -> str:
    """
    Derive OSCAL implementation status for a control across all hosts.

    Rules (most severe open finding wins):
      any open High/Very High  →  not-satisfied
      any open Medium/Low      →  partial
      no open findings         →  implemented
    """
    worst: int = -2   # sentinel: no findings at all
    for h in hosts:
        for sev in h.open_controls.get(control_id, []):
            rank = _SEVERITY_ORDER.get(sev.lower(), 0)
            if worst == -2 or rank > worst:
                worst = rank

    if worst == -2:
        return "implemented"    # control referenced but no open findings
    if worst >= _SEVERITY_ORDER["high"]:
        return "not-satisfied"
    return "partial"


def _compliance_remarks(
    control_id: str,
    compliance: list[ComplianceResult],
) -> str:
    """
    Build a remarks string from compliance results whose Reference field
    mentions this control ID.
    """
    control_upper = control_id.upper()
    relevant = [
        c for c in compliance
        if control_upper in c.reference.upper()
    ]
    if not relevant:
        return ""

    lines: list[str] = [f"Compliance evidence for {control_upper}:"]
    for c in relevant[:10]:   # cap at 10 entries to avoid oversized remarks
        lines.append(
            f"  [{c.result}] {c.compliance_test} "
            f"(Host: {c.host_key}, Plugin: {c.plugin_id})"
        )
    if len(relevant) > 10:
        lines.append(f"  ... and {len(relevant) - 10} more results.")
    return "\n".join(lines)


def _open_finding_summary(control_id: str, hosts: list[HostRecord]) -> str:
    """Summary string listing all open findings for a control."""
    lines: list[str] = []
    for h in hosts:
        sevs = h.open_controls.get(control_id)
        if sevs:
            sev_str = ", ".join(sorted(set(sevs), key=lambda s: -_SEVERITY_ORDER.get(s.lower(), 0)))
            lines.append(f"  {h.ip_address}: {sev_str}")
    if not lines:
        return "No open findings."
    return "Open findings:\n" + "\n".join(lines)


def _implemented_requirement(
    control_id: str,
    data: SystemData,
    config: SystemConfig,
) -> dict:
    """Build one OSCAL implemented-requirement for a single control."""
    system_comp_uuid = det_uuid(f"component:this-system:{config.system_name}")
    status = _implementation_status(control_id, data.hosts)

    comp_remarks_parts: list[str] = [
        _open_finding_summary(control_id, data.hosts),
    ]
    compliance_note = _compliance_remarks(control_id, data.compliance)
    if compliance_note:
        comp_remarks_parts.append(compliance_note)

    # Count affected hosts
    affected_hosts = [h for h in data.hosts if control_id in h.open_controls]

    by_component: dict = {
        "component-uuid": system_comp_uuid,
        "description": (
            f"Control {control_id.upper()} implementation status derived from "
            f"CLaaS Nessus scan data.  "
            f"Affected hosts with open findings: {len(affected_hosts)}."
        ),
        "props": [
            {
                "name": "implementation-status",
                "value": status,
                "ns": _NS_FEDRAMP,
            }
        ],
        "remarks": "\n\n".join(filter(None, comp_remarks_parts)),
    }

    req: dict = {
        "uuid": det_uuid(f"impl-req:{control_id}:{config.system_name}"),
        "control-id": control_id,
        "props": [
            {
                "name": "implementation-status",
                "value": status,
                "ns": _NS_FEDRAMP,
            }
        ],
        "set-parameters": [],
        "statements": [
            {
                "statement-id": f"{control_id}_smt",
                "uuid": det_uuid(f"stmt:{control_id}:{config.system_name}"),
                "description": (
                    f"REQUIRED — Provide the control implementation narrative for "
                    f"{control_id.upper()} per NIST SP 800-53 Rev 5.  "
                    f"CLaaS has automatically set the implementation status to "
                    f"'{status}' based on scan findings."
                ),
                "by-components": [by_component],
            }
        ],
        "by-components": [by_component],
    }

    return req


def _control_implementation(data: SystemData, config: SystemConfig) -> dict:
    """Build the full control-implementation section."""
    control_ids = sorted(data.all_control_ids)

    implemented_requirements = [
        _implemented_requirement(cid, data, config)
        for cid in control_ids
    ]

    return {
        "description": (
            "Control implementations derived from CLaaS Nessus QVD scan data. "
            "Each implemented-requirement covers one NIST SP 800-53 Rev 5 control "
            "referenced in the scanner findings.  "
            "Implementation narratives marked 'REQUIRED' must be completed by the "
            "system owner before ATO submission."
        ),
        "implemented-requirements": implemented_requirements,
    }


# ---------------------------------------------------------------------------
# system-implementation
# ---------------------------------------------------------------------------

def _system_implementation(data: SystemData, config: SystemConfig) -> dict:
    """Build the full system-implementation section."""
    # Index ports and software by IP for fast lookup
    ports_by_ip: dict[str, list[PortEntry]] = defaultdict(list)
    for pe in data.ports:
        if pe.ip_address:
            ports_by_ip[pe.ip_address].append(pe)

    sw_by_ip: dict[str, list[SoftwareEntry]] = defaultdict(list)
    for sw in data.software:
        sw_by_ip[sw.ip_address].append(sw)

    # Components
    components = [_this_system_component(config)]
    components += [_host_component(h, config) for h in data.hosts]
    components += _software_components(data.software)

    # Inventory items
    inventory_items = [
        _inventory_item(h, ports_by_ip, sw_by_ip, config)
        for h in data.hosts
    ]

    return {
        "users": _users(config),
        "components": components,
        "inventory-items": inventory_items,
    }


