def _back_matter(
    nessus_path: str,
    ppsm_path: Optional[str],
    software_path: Optional[str],
    compliance_path: Optional[str],
) -> dict:
    resources: list[dict] = [
        {
            "uuid": det_uuid(f"resource:nessus-qvd:{nessus_path}"),
            "title": "Nessus Main QVD",
            "description": "CLaaS Nessus vulnerability findings QVD — primary scan data source.",
            "rlinks": [{"href": nessus_path}],
        }
    ]
    if ppsm_path:
        resources.append(
            {
                "uuid": det_uuid(f"resource:ppsm-qvd:{ppsm_path}"),
                "title": "Nessus PPSM QVD",
                "description": "Port/Protocol/Service/MAC network inventory QVD.",
                "rlinks": [{"href": ppsm_path}],
            }
        )
    if software_path:
        resources.append(
            {
                "uuid": det_uuid(f"resource:software-qvd:{software_path}"),
                "title": "Nessus Software QVD",
                "description": "Installed software inventory QVD.",
                "rlinks": [{"href": software_path}],
            }
        )
    if compliance_path:
        resources.append(
            {
                "uuid": det_uuid(f"resource:compliance-qvd:{compliance_path}"),
                "title": "Nessus Compliance QVD",
                "description": "Policy Compliance family results QVD.",
                "rlinks": [{"href": compliance_path}],
            }
        )
    return {"resources": resources}


