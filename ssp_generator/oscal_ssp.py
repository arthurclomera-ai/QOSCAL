def _this_system_component(config: SystemConfig) -> dict:
    """The mandatory 'this-system' component representing the overall system."""
    return {
        "uuid": det_uuid(f"component:this-system:{config.system_name}"),
        "type": "this-system",
        "title": config.system_name,
        "description": config.system_description,
        "status": {"state": "operational"},
        "responsible-roles": [
            {"role-id": "system-owner"},
        ],
    }


def _host_component(host: HostRecord, config: SystemConfig) -> dict:
    """One component per unique host IP address."""
    comp_uuid = det_uuid(f"component:host:{host.ip_address}")
    props = [
        {"name": "asset-id",       "value": host.ip_address,      "ns": _NS_OSCAL},
        {"name": "asset-type",     "value": "computing-device",   "ns": _NS_OSCAL},
        {"name": "scan-type",      "value": "Nessus",             "ns": _NS_OSCAL},
        {"name": "credentialed-scan", "value": host.credentialed_scan, "ns": _NS_OSCAL},
    ]
    if host.host_key_id:
        props.append({"name": "mac-address", "value": host.host_key_id, "ns": _NS_OSCAL})
    if host.os_name:
        props.append({"name": "operating-system", "value": host.os_name, "ns": _NS_OSCAL})
    if host.netbios_name:
        props.append({"name": "netbios-name", "value": host.netbios_name, "ns": _NS_OSCAL})

    return {
        "uuid": comp_uuid,
        "type": "software",         # 'software' covers OS + application hosts
        "title": host.ip_address,
        "description": (
            f"Host {host.ip_address} identified by Nessus vulnerability scan. "
            f"Program Host ID: {host.program_host_id or 'N/A'}."
        ),
        "props": props,
        "status": {"state": "operational"},
    }


def _software_components(
    software: list[SoftwareEntry],
) -> list[dict]:
    """Unique software-type components derived from the Software QVD."""
    seen: set[str] = set()
    comps: list[dict] = []
    for sw in software:
        key = sw.software_key or sw.software
        if key in seen or not key:
            continue
        seen.add(key)
        comps.append(
            {
                "uuid": det_uuid(f"component:software:{key}"),
                "type": "software",
                "title": sw.software,
                "description": f"Software component identified by Nessus scan: {key}",
                "props": [
                    {"name": "software-identifier", "value": key, "ns": _NS_OSCAL},
                ],
                "status": {"state": "operational"},
            }
        )
    return comps


