def build_ssp(
    data: SystemData,
    config: SystemConfig,
    *,
    nessus_path: str = "",
    ppsm_path: Optional[str] = None,
    software_path: Optional[str] = None,
    compliance_path: Optional[str] = None,
) -> dict:
    """
    Build a complete OSCAL SSP document from QVD-derived data and system config.

    Args:
        data:            :class:`SystemData` loaded from QVD files.
        config:          :class:`SystemConfig` with user-supplied system metadata.
        nessus_path:     Path string for back-matter resource reference.
        ppsm_path:       Optional PPSM QVD path for back-matter.
        software_path:   Optional Software QVD path for back-matter.
        compliance_path: Optional Compliance QVD path for back-matter.

    Returns:
        A dict that serialises directly to a valid OSCAL SSP JSON document.
    """
    return {
        "system-security-plan": {
            "uuid": rand_uuid(),
            "metadata": _metadata(config),
            "import-profile": _import_profile(config),
            "system-characteristics": _system_characteristics(data, config),
            "system-implementation": _system_implementation(data, config),
            "control-implementation": _control_implementation(data, config),
            "back-matter": _back_matter(
                nessus_path, ppsm_path, software_path, compliance_path
            ),
        }
    }


def write_ssp(ssp: dict, output_path: str) -> None:
    """
    Serialise an SSP dict to a JSON file.

    Args:
        ssp:         Dict returned by :func:`build_ssp`.
        output_path: Destination file path (created or overwritten).
    """
    with open(output_path, "w", encoding="utf-8") as fh:
        json.dump(ssp, fh, indent=2, ensure_ascii=False)


def ssp_summary(ssp: dict) -> dict:
    """
    Return a brief summary dict for logging/display purposes.

    Args:
        ssp: Dict returned by :func:`build_ssp`.

    Returns:
        Dict with counts of components, inventory items, and implemented controls.
    """
    root = ssp["system-security-plan"]
    si = root.get("system-implementation", {})
    ci = root.get("control-implementation", {})
    return {
        "document_uuid":    root["uuid"],
        "oscal_version":    root["metadata"]["oscal-version"],
        "system_name":      root["metadata"]["title"],
        "components":       len(si.get("components", [])),
        "inventory_items":  len(si.get("inventory-items", [])),
        "controls":         len(ci.get("implemented-requirements", [])),
    }
