def _information_types(config: SystemConfig) -> list[dict]:
    types = []
    for it in config.information_types:
        obj: dict = {
            "uuid": det_uuid(f"info-type:{config.system_name}:{it.title}"),
            "title": it.title,
            "description": it.description,
            "confidentiality-impact": {
                "base": it.confidentiality_impact,
            },
            "integrity-impact": {
                "base": it.integrity_impact,
            },
            "availability-impact": {
                "base": it.availability_impact,
            },
        }
        if it.sp800_60_id:
            obj["categorizations"] = [
                {
                    "system": "https://doi.org/10.6028/NIST.SP.800-60v2r1",
                    "information-type-ids": [it.sp800_60_id],
                }
            ]
        types.append(obj)
    return types


def _system_characteristics(data: SystemData, config: SystemConfig) -> dict:
    system_uuid = config.system_id or det_uuid(f"system:{config.system_name}")
    short_name = config.system_name_short or config.system_name.split()[0]

    chars: dict = {
        "system-ids": [
            {
                "identifier-type": "https://ietf.org/rfc/rfc4122",
                "id": system_uuid,
            }
        ],
        "system-name": config.system_name,
        "system-name-short": short_name,
        "description": config.system_description,
        "props": [
            {
                "name": "scan-host-count",
                "value": str(len(data.hosts)),
                "ns": _NS_OSCAL,
                "remarks": "Number of unique host IPs found in the Nessus QVD.",
            }
        ],
        "security-sensitivity-level": config.security_sensitivity_level,
        "system-information": {
            "information-types": _information_types(config),
        },
        "security-impact-level": {
            "security-objective-confidentiality": config.confidentiality_impact,
            "security-objective-integrity": config.integrity_impact,
            "security-objective-availability": config.availability_impact,
        },
        "status": {
            "state": config.authorization_status,
        },
        "authorization-boundary": {
            "description": config.boundary_description,
        },
    }

    if config.date_authorized:
        chars["date-authorized"] = config.date_authorized

    return chars


