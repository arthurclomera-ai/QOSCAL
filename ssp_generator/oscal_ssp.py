def _now() -> str:
    """Current UTC time as RFC 3339."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _date_to_rfc3339(iso_date: Optional[str]) -> Optional[str]:
    """Convert YYYY-MM-DD string to RFC 3339 datetime string, or None."""
    if not iso_date:
        return None
    return f"{iso_date}T00:00:00Z"


