"""
OSCAL System Security Plan (SSP) document builder.

Produces a conformant OSCAL v1.2.1 system-security-plan JSON structure
from :class:`~ssp_generator.system_model.SystemData` (QVD-derived) and
:class:`~ssp_generator.system_model.SystemConfig` (user-supplied metadata).

Document structure
------------------
metadata
    Standard OSCAL metadata block — title, version, roles, parties.

import-profile
    Reference to the NIST 800-53 Rev 5 baseline profile (configurable).

system-characteristics
    System IDs, name, description, FIPS 199 categorisation, authorization
    boundary.  Information types default to a placeholder that must be
    updated to reference the applicable SP 800-60 Vol II type.

system-implementation
    users           — placeholder roles; must be updated with real user data.
    components      — one "this-system" component + one per unique host IP
                      from the Nessus main QVD + optional software components.
    inventory-items — one per unique host IP; enriched with port/protocol
                      props from the PPSM QVD when available.

control-implementation
    One implemented-requirement per unique NIST 800-53 control found in the
    Control_Temp field.  Satisfaction status is derived from open findings:

        satisfied          — control referenced in findings but no open findings
        partially-satisfied — open Low/Medium findings only
        not-satisfied      — any open High or Very High finding

    Evidence from the Compliance QVD (when provided) is attached as
    remarks on the by-component element.

back-matter
    Resource entry referencing the source QVD files.

References
----------
- OSCAL SSP metaschema: OSCAL/src/metaschema/oscal_ssp_metaschema.xml
- NIST SP 800-18 (SSP guidance)
- NIST SP 800-30 Rev 1 (risk vocabulary)
- OSCAL specification: https://pages.nist.gov/OSCAL
"""

